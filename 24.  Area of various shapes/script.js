// Rectangle Base*Height
var base = parseFloat(prompt("Enter Base = "));
var height = parseFloat(prompt("Enter Height = "));

var area = base * height;

document.write("Area = " + area);

// Triangle Base * Height/2
var base = parseFloat(prompt("Enter Base = "));
var height = parseFloat(prompt("Enter Height = "));

var area = (base * height)/2;

document.write("Area = " + area);

