// Breaking out of a for loop
for (let i = 0; i < 10; i++) {
  if (i === 5) {
    break; // Exits the loop when 'i' reaches 5
  }
  console.log(i);
}
// Output: 0 1 2 3 4

// Breaking out of a while loop
